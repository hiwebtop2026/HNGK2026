function extractSchoolBaseName(name) {
    const match = name.match(/^([^(\s]+)/);
    if (match) {
        return match[1].trim();
    }
    return name.trim();
}

function getSchoolLevel(schoolName) {
    const baseName = extractSchoolBaseName(schoolName);
    
    if (baseName in LEVEL_FIXES) {
        return LEVEL_FIXES[baseName];
    }
    
    if (UNIVERSITY_985.has(baseName)) {
        return '985';
    }
    if (UNIVERSITY_211.has(baseName)) {
        return '211';
    }
    if (UNIVERSITY_DOUBLE_FIRST.has(baseName)) {
        return '双一流';
    }
    return '普通本科';
}

function getSchoolNature(schoolName) {
    const baseName = extractSchoolBaseName(schoolName);
    
    if (PRIVATE_COLLEGES.has(baseName)) {
        return '民办';
    }
    
    for (const indicator of NATURE_INDICATORS['民办']) {
        if (schoolName.includes(indicator)) {
            return '民办';
        }
    }
    for (const indicator of NATURE_INDICATORS['中外合作办学']) {
        if (schoolName.includes(indicator)) {
            return '中外合作办学';
        }
    }
    return '公办';
}

function processScoreFiles(dirPath) {
    if (!fs.existsSync(dirPath)) {
        console.log(`❌ 目录不存在: ${dirPath}`);
        return { processed: 0, updated: 0 };
    }

    const files = fs.readdirSync(dirPath).filter(f => f.endsWith('.json'));
    let processed = 0;
    let updated = 0;

    console.log(`\n📁 处理目录: ${path.basename(dirPath)}`);
    console.log(`📊 待处理文件数: ${files.length}`);

    for (const file of files) {
        const filePath = path.join(dirPath, file);
        try {
            let content = fs.readFileSync(filePath, 'utf-8');
            if (content.charCodeAt(0) === 0xFEFF) {
                content = content.slice(1);
            }
            
            const data = JSON.parse(content);
            let hasChanges = false;

            for (const record of data) {
                const schoolName = record.school_name || record.name || '';
                const newLevel = getSchoolLevel(schoolName);
                const newNature = getSchoolNature(schoolName);
                if (record.level !== newLevel || record.nature !== newNature) {
                    record.level = newLevel;
                    record.nature = newNature;
                    hasChanges = true;
                }
            }

            if (hasChanges) {
                fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf-8');
                updated++;
            }
            processed++;

            if (processed % 100 === 0) {
                console.log(`   进度: ${processed}/${files.length} (已更新: ${updated})`);
            }
        } catch (e) {
            console.log(`   ⚠ 处理文件失败: ${file} - ${e.message}`);
        }
    }

    console.log(`✅ 处理完成: ${processed} 个文件，更新 ${updated} 个`);
    return { processed, updated };
}

