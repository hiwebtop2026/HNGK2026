data = pd.concat(dfs, ignore_index=True)

# 按院校专业组聚合
result = {}
for _, row in data.iterrows():
    code = str(row['院校专业组代码'])
    name = str(row['院校专业组名称'])
    subject = int(row['科目要求']) if pd.notna(row['科目要求']) else 0
    score = float(row['投档线']) if pd.notna(row['投档线']) else None
    year = int(row['年份'])
    
    key = f"{code}_{name}"
    if key not in result:
        province = get_province(name)
        level = get_school_level(name)
        result[key] = {
            'code': code,
            'name': name,
            'subject': subject,
            'province': province,
            'level': level,
            'score2025': None,
            'score2024': None,
            'score2023': None,
        }
    
    if year == 2025:
        result[key]['score2025'] = score
    elif year == 2024:
        result[key]['score2024'] = score
    elif year == 2023:
        result[key]['score2023'] = score

# 统计各省份和层次分布
provinces = {}
levels = {}
for item in result.values():
    provinces[item['province']] = provinces.get(item['province'], 0) + 1
    levels[item['level']] = levels.get(item['level'], 0) + 1

print('院校层次分布:')
