def get_province(name: str) -> str:
    """根据院校名称推断省份"""
    for key, province in PROVINCE_MAP.items():
        if key in name:
            return province
    return '其他'

def get_school_level(name: str) -> str:
    """获取院校层次"""
    # 985
    for keyword in NINE_EIGHT_FIVE:
        if keyword in name:
            return '985'
    # 211
    for keyword in TWO_ONE_ONE:
        if keyword in name:
            return '211'
    # 双一流
    for keyword in DOUBLE_FIRST_CLASS:
        if keyword in name:
            return '双一流'
    # 普通本科
    return '普通本科'

