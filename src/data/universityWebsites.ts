export function getUniversityWebsite(schoolName: string): string | null {
  const cleanedName = schoolName.replace(/\(\d+\)/g, '').replace(/（\d+）/g, '').trim();
  
  if (UNIVERSITY_WEBSITES[cleanedName]) {
    return UNIVERSITY_WEBSITES[cleanedName];
  }
  
  for (const [name, url] of Object.entries(UNIVERSITY_WEBSITES)) {
    if (cleanedName.includes(name) || name.includes(cleanedName)) {
      return url;
    }
  }
  
  return null;
}
